export function t(key, locale = 'en') {
  const keys = key.split('.')
  let val = translations[locale]
  for (const k of keys) {
    if (!val) return key
    val = val[k]
  }
  return val || key
}

// Get localized text from lab JSON (which uses {en, ru} objects)
export function tLab(obj, locale = 'en') {
  if (!obj) return ''
  if (typeof obj === 'string') return obj
  return obj[locale] || obj.en || ''
}

export default translations
